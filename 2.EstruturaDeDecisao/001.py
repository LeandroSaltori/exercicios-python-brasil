"""
Faça um Programa que peça dois números e imprima o maior deles.
"""

n1 = float(input('Digite um número: '))
n2 = float(input('Digite um número: '))

if n1 > n2:
    print(f'Os números digitados foram {n1} e {n2}. O maior deles é: {n1}')
else:
    print(f'Os números digitados foram {n1} e {n2}. O maior deles é: {n2}')
