"""
Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
"""

a = float(input('Digite o lado do quadrado: '))
area = a ** 2
dobro = area * 2
print(f'A area do quadrado é {area}, o dobro dela é {dobro}!')